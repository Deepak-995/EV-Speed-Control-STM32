uint32_t Read_HCSR04_Distance(void)
{
    uint32_t start, stop;
    uint32_t timeout = 1000000;

    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);
    DWT_Delay_us(2);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);
    DWT_Delay_us(10);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);

    // Wait for echo pin (PA7) to go high
    while (!HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_7))
    {
        if (timeout-- == 0)
            return 0;
    }
    start = DWT->CYCCNT;

    timeout = 1000000;
    while (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_7))
    {
        if (timeout-- == 0)
            return 0;
    }
    stop = DWT->CYCCNT;

    uint32_t us = (stop - start) / (HAL_RCC_GetHCLKFreq() / 1000000);
    return us / 58;
}

float Read_Potentiometer(void)
{
    HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
    uint32_t adc_val = HAL_ADC_GetValue(&hadc1);
    HAL_ADC_Stop(&hadc1);
    float voltage = (adc_val * 3.0f) / 4095.0f;
    return voltage;
}
